// Функция для генерации промта изображения
function imagePrompt(theme) {
    if (!theme) {
        throw new Error("Тема не должна быть пустой"); // Обработка ошибок
    }
    return `Создайте красивое изображение на тему: ${theme}. Убедитесь, что изображение высококачественное и подходит для публикации в Instagram.`;
}

// Экспортируем функции
module.exports = { imagePrompt };